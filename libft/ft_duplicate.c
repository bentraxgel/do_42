#include "libft.h"

int	ft_duplicate(int *arr, size_t len)
{
	size_t	i;
	size_t	j;

	i = 0;
	while (i < len)
	{
		j = i + 1;
		while(j < len)
		{
			if (arr[i] == arr[j]){
				return(1);}
			j++;
		}
		i++;
	}
	return (0);
}